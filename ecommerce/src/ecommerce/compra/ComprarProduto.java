    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        bd.conecta();
        String sql = "select nome from cliente where cpf = '"+cpfCliente.getText()+"'";
        ResultSet rs = bd.consulta(sql);
        String nome = "";
        try {
            while(rs.next()){
                nome=rs.getString(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ComprarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        nomeCliente.setText(nome);
        
        sql = "select descricao from produto where id = '"+idProduto.getText()+"'";
        rs = bd.consulta(sql);
        String nomep = "";
        try {
            while(rs.next()){
                nomep=rs.getString(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ComprarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        nomeProduto.setText(nomep);
        
        sql = "select preco from produto where id = '"+idProduto.getText()+"'";
        rs = bd.consulta(sql);
        String valor = "";
        try {
            while(rs.next()){
                valor=rs.getString(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ComprarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        precoProduto.setText(valor);
        
        sql = "select preco from produto where id = '"+idProduto.getText()+"'";
        rs = bd.consulta(sql);
       
        try {
            while (rs.next()){
                valorAtual = valorAtual + rs.getFloat("preco");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ComprarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        valorTotalText.setText(String.valueOf(valorAtual));
    }//GEN-LAST:event_jButton1MouseClicked
     float valorAtual = 0;
