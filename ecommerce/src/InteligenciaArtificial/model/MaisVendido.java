
package InteligenciaArtificial.model;


public class MaisVendido {
    Integer idCategoria;
    String vezesVendido;
    Integer pontuacao;

    public Integer getIdCategoria() {
        return idCategoria;
    }

    public void setIdCategoria(Integer idCategoria) {
        this.idCategoria = idCategoria;
    }

    public String getVezesVendido() {
        return vezesVendido;
    }

    public void setVezesVendido(String vezesVendido) {
        this.vezesVendido = vezesVendido;
    }

    public Integer getPontuacao() {
        return pontuacao;
    }

    public void setPontuacao(Integer pontuacao) {
        this.pontuacao = pontuacao;
    }
    
    
}
